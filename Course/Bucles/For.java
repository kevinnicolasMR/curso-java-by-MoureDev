package Course.Bucles;

public class For {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        String[] newArray = {"Kevin", "Medina", "Robles", "Nicolas", "Pedro"};

        for (int i = 0; i < newArray.length; i++){
            System.out.println(newArray[i]);
        }



    }
}
